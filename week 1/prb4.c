#include<stdio.h>
int main()
{
int length=2;
int width=3;
int height=4;
int volume=length*width*height;
printf("Length of parallelepiped %d\n",length);
printf("Width of parallelepiped %d\n",width);
printf("Height of parallelepiped %d\n",height);
printf("The volume of parallelepiped is %d\n",volume);
return 0;
}